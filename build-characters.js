/* LexusLight — regenerates data-characters.js from Characters/<folder>/description.md
   Run this with: node build-characters.js
   whenever you edit a description.md, so the site (both on GitHub Pages and opened
   directly as a local file) picks up the change. Browsers won't let a page opened
   via file:// fetch() other local files, so the text has to be baked in ahead of
   time instead of read live. */

const fs = require('fs');
const path = require('path');

// order + visuals (color / initials / photo / game-tag) — edit here to add,
// remove, or reorder a character card. bio text itself lives in the .md files.
const ROSTER = [
  { folder: 'Lexus-Luminera', picClass: 'char-picture--photo',  image: 'images/Avatar.jpg', tag: false },
  { folder: 'Candle-Cat',     picClass: 'char-picture--yellow', initials: 'CC', tag: true },
  { folder: 'Lucky-Weiss',    picClass: 'char-picture--orange', initials: 'LW', tag: true },
  { folder: 'Thin-Green-Duke',picClass: 'char-picture--green',  initials: 'GD', tag: true },
  { folder: 'Mint',           picClass: 'char-picture--teal',   initials: 'M',  tag: true },
  { folder: 'Caspian',        picClass: 'char-picture--purple', initials: 'C',  tag: true },
  { folder: 'Sylvia',         picClass: 'char-picture--magenta',initials: 'S',  tag: true },
  { folder: 'Vortex-Sparks',  picClass: 'char-picture--silver', initials: 'VS', tag: true },
  { folder: 'Elric-Rose',     picClass: 'char-picture--crimson',initials: 'ER', tag: true },
  { folder: 'Queen-Victoria', picClass: 'char-picture--indigo', initials: 'QV', tag: true }
];

function parseCharacterMd(text) {
  const nameMatch = text.match(/^#\s+(.+)$/m);
  const roleMatch = text.match(/\*\*Role:\*\*\s*(.+)/);
  const paragraphs = text.trim().split(/\n\s*\n/).map(p => p.trim()).filter(Boolean);
  const blurb = paragraphs.length ? paragraphs[paragraphs.length - 1] : '';
  return {
    name: nameMatch ? nameMatch[1].trim() : '',
    role: roleMatch ? roleMatch[1].trim() : '',
    blurb
  };
}

const characters = ROSTER.map(entry => {
  const mdPath = path.join(__dirname, 'Characters', entry.folder, 'description.md');
  const text = fs.readFileSync(mdPath, 'utf8');
  const { name, role, blurb } = parseCharacterMd(text);
  return { ...entry, name, role, blurb };
});

const out = `/* LexusLight — character roster. AUTO-GENERATED by build-characters.js — do not
   hand-edit. To change a bio: edit Characters/<folder>/description.md, then run
   \`node build-characters.js\` to rebuild this file. To reorder/add/remove a card
   or change its color/image, edit the ROSTER list inside build-characters.js. */
const CHARACTERS = ${JSON.stringify(characters, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, 'data-characters.js'), out);
console.log(`data-characters.js rebuilt (${characters.length} characters).`);
